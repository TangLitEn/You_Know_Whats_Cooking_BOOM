#include <bits/stdc++.h>
#define ll long long 
#define db double

using namespace std;

void solve(void)
{
    ll N; 
    cin >> N; // permutation length
    cout<< N <<" ";
    for (ll i=1;i<N;i++) cout<<i<<" ";
    cout<<endl;
}

int main(void)
{
    ll n;
    cin>>n;
    while(n--) solve();
    return 0;
}
